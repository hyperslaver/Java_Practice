package com.lang_006.getter_setter;

public class Car {
    private String color;
    private String speed;

    public void setColor(String color) {
        this.color = color;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }

    public String getColor() {
        return color;
    }

    public String getSpeed() {
        return speed;
    }
}

